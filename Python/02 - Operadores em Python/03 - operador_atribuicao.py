# Operadores de atribuição em Python são usados para atribuir valores a variáveis. O operador de atribuição mais comum é o =, mas existem outros que combinam operações matemáticas com atribuição.

# Exemplos:

# Atribuição simples
x = 10  # x recebe o valor 10
print(x)

# Atribuição com adição
x += 5  # Equivalente a: x = x + 5 (agora x vale 15)
print(x)
# Atribuição com subtração
x -= 3  # Equivalente a: x = x - 3 (agora x vale 12)
print(x)
# Atribuição com multiplicação
x *= 2  # Equivalente a: x = x * 2 (agora x vale 24)
print(x)
# Atribuição com divisão
x /= 4  # Equivalente a: x = x / 4 (agora x vale 6.0)
print(x)
# Atribuição com módulo
x %= 5  # Equivalente a: x = x % 5 (agora x vale 1.0)
print(x)

