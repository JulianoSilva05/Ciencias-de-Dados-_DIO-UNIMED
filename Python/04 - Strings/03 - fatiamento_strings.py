#Fatiamento de Strings
# Fatiamento de strings é uma técnica ultilizada para retornar substrings(parte da String original), informando inicio (start), fim (stop) e passo(step): [start:stop,[,step]]
nome  ="Juliano Fernando da Silva"

print(nome[0])
print(nome[-1])
print(nome[:7])
print(nome[8:16])
print(nome[8:16:2])
print(nome[:])
print(nome[::-1])