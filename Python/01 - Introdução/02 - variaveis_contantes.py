# Variáveis em Python
# Uma variável é um nome que armazena um valor que pode mudar durante a execução do programa.

idade = 25        # variável do tipo inteiro
nome = "Juliano"    # variável do tipo string
altura = 1.74     # variável do tipo float

print(f"Nome: {nome}")
print(f"Idade: {idade}")
print(f"Altura: {altura}")

# Constantes em Python
# Por convenção, constantes são escritas em letras maiúsculas.
# O valor de uma constante não deve ser alterado durante o programa.

PI = 3.14159
GRAVIDADE = 9.8

print(f"Valor de PI: {PI}")
print(f"Valor da gravidade: {GRAVIDADE}")

# Observação: Python não impede a alteração de constantes, mas é uma boa prática não mudar seus valores.